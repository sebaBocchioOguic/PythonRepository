# While Iteration in Python

# While allows to iterate a 'x' number of sentences while a condition is valid

# Counter i
i = 1

# While Sentence iteretes until i > 6 
while i <= 5:
    print(i)
    i+=1
    
    # Breaks when it reaches 3
    if i == 3:
        break


# WHILE Sentence
# while <condition>:
#    print()
# It is very important to add a break sentence into the while block, to avoid infinite loops

